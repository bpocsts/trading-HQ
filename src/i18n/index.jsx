const I18nContext = createContext(null)

function getMessage(locale, key) {
  return key.split('.').reduce((value, part) => value?.[part], messages[locale])
}

export function I18nProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem('trading-journal-language') || 'en')

  useEffect(() => {
    localStorage.setItem('trading-journal-language', language)
    document.documentElement.lang = language
  }, [language])

  const value = useMemo(() => ({
    language,
    setLanguage,
    toggleLanguage: () => setLanguage((prev) => prev === 'en' ? 'th' : 'en'),
    t: (key) => getMessage(language, key) ?? key,
  }), [language])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error('useI18n must be used within I18nProvider')
  return context
}
